#include "main.h"

/**
 *add - function that retirn sum of two numbers
 *@a: inpu
 *@b: input
 *Return: sum
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 *sub - returns the substraction of two numbers
 *@a: input
 *@b: input
 *Return: substraction
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 *div - returns the division of two numbers
 *@a: input
 *@b: input
 *Return: always 0 on success
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 *mul - return the product of two numbers
 *@a: input
 *@b: input
 *Return: always 0 on success
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 *mod - returns the remainder
 *@a: input
 *@b: input
 *Return: always 0 on success
 */

int mod(int a, int b)
{
	return (a % b);
}
